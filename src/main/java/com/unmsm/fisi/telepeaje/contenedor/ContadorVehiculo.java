public class ContadorVehiculo {
    private String sFecha;
    private int nContador;
    private String sIdentificador;

    public String getsFecha() {
        return sFecha;
    }

    public void setsFecha(String sFecha) {
        this.sFecha = sFecha;
    }

    public int getnContador() {
        return nContador;
    }

    public void setnContador(int nContador) {
        this.nContador = nContador;
    }

    public String getsIdentificador() {
        return sIdentificador;
    }

    public void setsIdentificador(String sIdentificador) {
        this.sIdentificador = sIdentificador;
    }
  
}
